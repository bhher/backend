package a1114;

public class Bitshift {
    public static void main(String[] args) {
        int num1 =1;
        int result1 = num1 << 3; // 좌로 3칸이동 
        int result2 = num1 * (int) Math.pow(2,3);
        System.out.println("result1 : "+ result1);
        System.out.println("result2 : "+ result2);
    }
}
